const calcSum = (num1, num2) =>{
    return num1 + num2;
}

const calcSubstract = (num1, num2) =>{
    return num1 - num2;
}

const calcMultiply = (num1, num2) =>{
    return num1 * num2;
}

const calcDivide = (num1, num2) =>{
    return num1 / num2;
}

module.exports = {calcSum, calcSubstract, calcMultiply, calcDivide};